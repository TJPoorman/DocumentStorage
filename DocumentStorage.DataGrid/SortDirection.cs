namespace DocumentStorage.DataGrid;

/// <summary>
/// Specifies the direction for sorting data.
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// Indicates that the data should be sorted in ascending order.
    /// </summary>
    Ascending,

    /// <summary>
    /// Indicates that the data should be sorted in descending order.
    /// </summary>
    Descending,
}